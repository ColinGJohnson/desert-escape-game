package dev.cgj.desertescape;

public class Inventory {
  private int rockets;
  private int nitro;
  private int shield;

  public int getRockets() {
    return rockets;
  }

  public void setRockets(int rockets) {
    this.rockets = rockets;
  }

  public int getNitro() {
    return nitro;
  }

  public void setNitro(int nitro) {
    this.nitro = nitro;
  }

  public int getShield() {
    return shield;
  }

  public void setShield(int shield) {
    this.shield = shield;
  }
}
